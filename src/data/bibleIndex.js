/** 按译本语言返回书卷名（简体/繁体/英文） */
export function getBookTitle(bookId, lang) {
  const book = bibleIndex[bookId]
  if (!book) return ''
  if (lang === 'chs') return book.titleChs
  if (lang === 'en') return book.name
  return book.title
}

export function parseChapterParam(chapterParam) {
  return parseInt(chapterParam, 10)
}

export function chapterToParam(chapter) {
  return String(chapter)
}
